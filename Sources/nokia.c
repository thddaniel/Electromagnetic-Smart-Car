/*-----------------------------------------------------------------------
LCD_write_english_String  : 英文字符串显示函数

输入参数：*s      ：英文字符串指针；
          X、Y    : 显示字符串的位置,x 0-83 ,y 0-5

编写日期          ：2004-8-10 
最后修改日期      ：2004-8-10 		
-----------------------------------------------------------------------*/
void LCD_write_english_string(uchar8 X,uchar8 Y,uchar8 *s)
{
    LCD_set_XY(X,Y);
    while (*s) 
    {
      LCD_write_char(*s);
      s++;
    }
}
