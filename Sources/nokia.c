/*********************************************************** 
函数名称：LCD_write_byte
函数功能：模拟SPI接口时序写数据/命令LCD
入口参数：data    ：写入的数据；
          command ：写数据/命令选择；
出口参数：无 
备 注： 管脚D/C(LCD_DC)用于选择写的是命令(D/C=0)还是数据(D/C=1)
***********************************************************/
void LCD_write_byte(uchar8 dat, uchar8 command)
{
    uchar8 i; 
    CE=0;	
    DC=command;	
    for(i=0;i<8;i++)
    { 
      if(dat&0x80)
      	SDIN=1;
      else
      	SDIN=0;
      	delay_1ms(1);
      dat=dat<<1;	
      	delay_1ms(1);	
      SCLK=0; 
      	delay_1ms(1);		
      SCLK=1; 
      	delay_1ms(1);
    }	
    DC=1;	
    CE=1;	
    SDIN=1;
}
/*********************************************************** 
函数名称：LCD_set_XY
函数功能：设置LCD坐标函数
入口参数：X       ：0－83
          Y       ：0－5
出口参数：无 
备 注： 
***********************************************************/
void LCD_set_XY(uchar8 x, uchar8 y) 
{
    LCD_write_byte(0x40 | y, 0);//lie
    LCD_write_byte(0x80 | x, 0);
}
/*------------------------------------------
LCD_clear: LCD清屏函数
编写日期：20080918 
--------------------------------------------*/
void LCD_clear(void) 
{
    uint32 i;
    LCD_write_byte(0x0c, 0);			
    LCD_write_byte(0x80, 0);			

    for (i=0; i<504; i++)
      LCD_write_byte(0, 1);	
}
/*********************************************************** 
函数名称：LCD_init
函数功能：5110初始化
入口参数：无
出口参数：无 
备 注： 
***********************************************************/
void LCD_init(void) 
{
    RST = 0;
    delay_1ms(1);
    RST = 1;
    delay_1ms(1);

    CE = 0;
    delay_1ms(1);
		// 使能LCD
    CE = 1;
    delay_1ms(1);

    LCD_write_byte(0x21, 0);	// 使用扩展命令设置LCD模式
    LCD_write_byte(0xc8, 0);	// 设置偏置电压
    LCD_write_byte(0x06, 0);	// 温度校正
    LCD_write_byte(0x10, 0);	// 1:100
    LCD_write_byte(0x20, 0);	// 使用基本命令
    LCD_clear();	            // 清屏
    LCD_write_byte(0x0c, 0);	// 设定显示模式，正常显示
    
    CE = 0;
    
    BL = 1;   //背景灯光
}


